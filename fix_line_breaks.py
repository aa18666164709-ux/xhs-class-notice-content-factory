from pathlib import Path


root = Path(r"F:\Obsidian\小红书爆款迁移内容工厂")


def write_lines(rel_path, lines):
    path = root / rel_path
    path.parent.mkdir(parents=True, exist_ok=True)
    text = "\n".join(lines) + "\n"
    path.write_text(text, encoding="utf-8")
    actual = path.read_text(encoding="utf-8")
    count = len(actual.splitlines())
    print(rel_path, count)
    if count < 5:
        raise RuntimeError(f"{rel_path} line count too small: {count}")


